# String Methods

name = "Gautam Singh Rathore"
print(len(name))
print(name.upper()) # Does not modify the original string
print(name.lower())
# Returns the first index or -1 for non-availability
print(name.find('R'))
print(name.replace('Singh' ,  'Anil Singh')) # Does not modify the original string
# In operator produces boolean value
print("Singh" in name)
